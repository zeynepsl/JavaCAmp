
public class Student {
	int id;
	String firstName;
	String lastName;
	String email;
	int password;
	
	public Student() {
		
	}
	
	public Student(int id, String firstName, String lastName, String email, int password) {
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.password = password;
	}
}
